  <script>
    document.addEventListener('DOMContentLoaded', () => {
      const carouselTrack = document.querySelector('.carousel-track');
      const vehicleCards = document.querySelectorAll('.vehicle-card');
      const dots = document.querySelectorAll('.dot');
      const totalCards = vehicleCards.length;
      let currentIndex = 0;
      let autoSlideInterval;

      // Función para mostrar la tarjeta actual
      function showCard(index) {
          carouselTrack.style.transform = `translateX(-${index * 100}%)`;
          dots.forEach((dot, i) => {
              dot.classList.toggle('active', i === index);
          });
          currentIndex = index;
      }

      // Función para avanzar al siguiente slide
      function nextSlide() {
          currentIndex = (currentIndex + 1) % totalCards;
          showCard(currentIndex);
      }

      // Función para iniciar el deslizamiento automático
      function startAutoSlide() {
          autoSlideInterval = setInterval(nextSlide, 5000); // Cambia cada 5 segundos
      }

      // Función para detener el deslizamiento automático (útil si el usuario interactúa)
      function stopAutoSlide() {
          clearInterval(autoSlideInterval);
      }

      // Event listeners para los puntos de navegación
      dots.forEach(dot => {
          dot.addEventListener('click', (event) => {
              const slideIndex = parseInt(event.target.dataset.slide);
              stopAutoSlide(); // Detener auto-slide al hacer clic
              showCard(slideIndex);
              startAutoSlide(); // Reiniciar auto-slide
          });
      });

      // Iniciar el carrusel
      showCard(currentIndex);
      startAutoSlide();

      // Opcional: Pausar el carrusel al pasar el ratón por encima
      carouselTrack.addEventListener('mouseenter', stopAutoSlide);
      carouselTrack.addEventListener('mouseleave', startAutoSlide);
  });
  </script>

